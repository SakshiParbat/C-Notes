//ASCII Value Of A Character To Int In C++ (For Small Alphabets)
#include <iostream>
using namespace std;
int main()
{
    char w = 'a';
    cin>>w;
    cout<<"The Small a ASCII Value Of IS: "<<int(w)<<endl;
    char x = 'b';
    cin>>x;
    cout<<"The Small b ASCII Value Of Is: "<<int(x)<<endl;
    char y = 'c';
    cin>>y;
    cout<<"The Small c ASCII Value Of Is: "<<int(y)<<endl;
    char z = 'd';
    cin>>z;
    cout<<"The Small d ASCII Value Of Is: "<<int(z)<<endl;
    return 0;
}