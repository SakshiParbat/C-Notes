//ASCII VALUE OF A Character To Int In C++(For Big Alphabets)
#include <iostream>
using namespace std;
int main()
{
    char ch;
    cin>>ch;
    cout<<"The 1st ASCII Value Of"<<ch<<"Is: "<<int(ch)<<endl;
    char w;
    cin>>w;
    cout<<"The 2nd ASCII Value Of"<<w<<"Is: "<<int(w)<<endl;
    char x;
    cin>>x;
    cout<<"The 3rd ASCII Value Of"<<x<<"Is: "<<int(x)<<endl;
    char y;
    cin>>y;
    cout<<"The 4th ASCII Value Of"<<y<<"Is: "<<int(y)<<endl;
    char z;
    cin>>z;
    cout<<"The 5th ASCII Value Of"<<z<<"Is: "<<int(z)<<endl;

    return 0;
}