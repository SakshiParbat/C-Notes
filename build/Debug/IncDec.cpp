//Increment Or Derement Operateor In C++
#include <iostream>
using namespace std;
int main()
{
    int a = 15;
    cout<<"a ="<<a++;
    cout<<endl;
    cout<<a<<endl;
    int b = 38;
    cout<<"b ="<<b--;
    cout<<endl;
    cout<<b<<endl;
    int c = 20;
    cout<<"c ="<<++c;
    cout<<endl;
    cout<<c<<endl;
    int d = 25;
    cout<<"d ="<<--d;
    cout<<endl;
    cout<<d;
    return 0;

}